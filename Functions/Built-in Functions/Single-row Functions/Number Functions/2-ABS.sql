SELECT 
    -10 AS number,  -- Número original negativo
    ABS(-10) AS absolute_value
    -- A função ABS (abreviação de "absolute") retorna o valor absoluto do número,
    -- ou seja, transforma um número negativo em positivo e mantém positivos inalterados.
    -- Isso é útil em situações onde só o valor numérico importa, independentemente do sinal,
    -- como em distâncias, magnitudes ou comparações matemáticas.
